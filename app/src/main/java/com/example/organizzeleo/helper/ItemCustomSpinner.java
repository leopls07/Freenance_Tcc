package com.example.organizzeleo.helper;

public class ItemCustomSpinner {

    private String SpinnerString;
    private int SpinnerImagem;


    public ItemCustomSpinner(String spinnerString, int spinnerImagem) {
        SpinnerString = spinnerString;
        SpinnerImagem = spinnerImagem;
    }

    public String getSpinnerString() {
        return SpinnerString;
    }


    public int getSpinnerImagem() {
        return SpinnerImagem;
    }

}
